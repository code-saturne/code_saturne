    def parse_gui_expression(self,
                             expression,
                             req,
                             known_symbols,
                             func_type,
                             need_for_loop = False):

        usr_code = ''
        usr_defs = ''
        if_loop = False

        tab = '  '
        ntabs = 3

        # Parse the Mathematical expression and generate the C block code
        exp_lines = expression.split("\n")
        nreq = len(req)
        for l in exp_lines:
            lidx = exp_lines.index(l)
            if len(l) > 0:
                lf = l.split('=')
                if len(lf) > 1 and l[0] != "#":
                    if lf[0].strip() not in known_symbols:
                        known_symbols.append(lf[0].strip())
                        # Test whether we are inside an if loop or not!
                        if if_loop:
                            usr_defs += 2*tab + 'cs_real_t %s = -1.;\n' % (lf[0])
                        else:
                            l = 'const cs_real_t '+l

                    elif lf[0].strip() in req:
                        if func_type == 'vol':
                            new_v = 'f->val[c_id]'

                        elif func_type == 'bnd':
                            ir = req.index(lf[0].strip())
                            if need_for_loop:
                                new_v = 'new_vals[%d * bz->n_elts + e_id]' % (ir)
                            else:
                                new_v = 'new_vals[%d]' % (ir)

                        elif func_type == 'src':
                            if nreq > 1:
                                ir = req.index(lf[0].strip())
                                new_v = 'new_vals[%d * e_id + %d]' % (nreq, ir)
                            else:
                                new_v = 'new_vals[e_id]'

                        elif func_type == 'ini':
                            if nreq > 1:
                                ir = req.index(lf[0].strip())
                                new_v = 'new_vals[%d * e_id + %d]' % (nreq, ir)
                            else:
                                new_v = 'new_vals[e_id]'

                        l = new_v + ' = ' + lf[1]

                # Check for power functions
                if '^' in l:
                    l = self.search_and_replace_power(l)

                # If loop
                if l[:2] == 'if':
                    if_loop = True
                    if l[-1] != '{' and exp_lines[lidx+1][0] != '{':
                        l = l + ' {'

                if 'else' in l:
                    if l[0] != '}' and if_loop and exp_lines[lidx-1][-1] != '}':
                        l = '} '+l

                    if l[-1] != '{' and exp_lines[lidx+1][0] != '{':
                        l = l + ' {'
                    if_loop = True

                if l[0] == '}':
                    if_loop = False
                    ntabs -= 1

                usr_code += '\n'
                if l[-1] == '}':
                    usr_code += ntabs*tab + l[:-1]
                    ntabs -= 1
                    usr_code += '\n' + ntabs*tab + '}'
                    if_loop = False
                else:
                    usr_code += ntabs*tab + l

                if l[-1] == '{':
                    ntabs += 1

            else:
                usr_code += '\n'

        if if_loop and usr_code[-1] != '}':
            usr_code += '\n' + (ntabs-1)*tab + '}\n'
        else:
            usr_code += '\n'

        # Change comments symbol from # to //
        usr_code = usr_code.replace('#', '//')

        return usr_code, usr_defs

