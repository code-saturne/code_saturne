C --------
c@foncb
CFONC
CFONC ROUTINE UTILISATEUR POUR LA METHODE ALE :
CFONC                   MODIFICATION DE LA VISCOSITE DE MAILLAGE
CFONC
CFONC  Cette routine est appelee au debut de chaque pas de temps
CFONC
CFONC  On peut modifier la viscosite de maillage pour limiter
CFONC    la deformation de maillage dans les zones sensibles (couche
CFONC    limite par exemple)
CFONC
CFONC  Si IORTVM = 0, la viscosite de maillage est isotrope. Il suffit
CFONC    de remplir VISCMX.
CFONC  Si IORTVM = 1, la viscosite de maillage est orthotrope, on
CFONC    remplit VISCMX, VISCMY et VISCMZ
CFONC
CFONC  Les tableaux sont initialises a 1 au premier pas de temps.
CFONC
