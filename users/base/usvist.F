                        SUBROUTINE USVIST
C                       *****************
C     -------------------------------------------------------------
     & ( IDBIA0 , IDBRA0 ,
     &   NDIM   , NCELET , NCEL   , NFAC   , NFABOR , NFML   , NPRFML ,
     &   NNOD   , LNDFAC , LNDFBR , NCELBR ,
     &   NVAR   , NSCAL  , NPHAS  , NCEPDP , NCESMP ,
     &   NIDEVE , NRDEVE , NITUSE , NRTUSE , IPHAS  ,
     &   IFACEL , IFABOR , IFMFBR , IFMCEL , IPRFML ,
     &   IPNFAC , NODFAC , IPNFBR , NODFBR , ICEPDC , ICETSM , ITYPSM ,
     &   IDEVEL , ITUSER , IA     ,
     &   XYZCEN , SURFAC , SURFBO , CDGFAC , CDGFBO , XYZNOD , VOLUME ,
     &   DT     , RTP    , RTPA   , PROPCE , PROPFA , PROPFB ,
     &   COEFA  , COEFB  , CKUPDC , SMACEL ,
     &   W1     , W2     , W3     , W4     ,
     &   W5     , W6     , W7     , W8     ,
     &   RDEVEL , RTUSER , RA     )
C     -------------------------------------------------------------
C***********************************************************************
C FONCTION :
C --------
c@foncb
CFONC
CFONC ROUTINE UTILISATEUR : MODIFICATION DE LA VISCOSITE TURBULENTE
CFONC
CFONC  Cette routine est appelee au debut de chaque pas de temps
CFONC     apres le calcul de la viscosite turbulente
CFONC    (les grandeurs physiques ont ete calculees dans USPHYV)
CFONC
CFONC  On peut modifier la viscosite turbulente VISCT (kg/(m s))
CFONC
CFONC  La modification de la viscosite turbulente peut conduire a des
CFONC     ecarts tres importants sur la solution et eventuellement
CFONC     A DES RESULTATS ERRONES.
CFONC
CFONC
CFONC
CFONC  L'UTILISATION DE CE SOUS PROGRAMME EST DONC RESERVEE A
CFONC
CFONC             DES UTILISATEURS TRES AVERTIS
CFONC
CFONC
CFONC
